CREATE EXTENSION IF NOT EXISTS citext;
-- ============================================================
-- 1. USERS
-- ============================================================
CREATE TABLE users (
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    phone_number VARCHAR(20),
    email CITEXT NOT NULL,
    avatar_url TEXT,
    password_hash VARCHAR(255) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_users_email
        UNIQUE (email),

    CONSTRAINT uk_users_phone_number
        UNIQUE (phone_number),

    CONSTRAINT ck_users_status
        CHECK (
            status IN (
                'ACTIVE',
                'INACTIVE',
                'DISABLED'
            )
        )
);


-- ============================================================
-- 2. ORGANIZATION
-- ============================================================

CREATE TABLE organization (
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    slug VARCHAR(150) NOT NULL,
    description VARCHAR(500),
    logo_url TEXT,

    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT uk_organization_slug
        UNIQUE (slug),

    CONSTRAINT ck_organization_status
        CHECK (
            status IN (
                'ACTIVE',
                'INACTIVE',
                'SUSPENDED'
            )
        )
);


CREATE INDEX idx_organization_status
    ON organization(status);

-- ============================================================
-- 3. DEPARTMENT
-- ============================================================

CREATE TABLE department (
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

    organization_id int NOT NULL,

    name VARCHAR(255) NOT NULL,
    description VARCHAR(500),

    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',

    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_department_organization
        FOREIGN KEY (organization_id)
        REFERENCES organization(id)
        ON DELETE CASCADE,

    CONSTRAINT uk_department_name
        UNIQUE (organization_id, name),
    CONSTRAINT uk_department_id_organization
        UNIQUE (id, organization_id),

    CONSTRAINT ck_department_status
        CHECK (
            status IN (
                'ACTIVE',
                'INACTIVE'
            )
        )
);


CREATE INDEX idx_department_organization_status
    ON department(organization_id, status);


-- ============================================================
-- 4. ORGANIZATION MEMBER
-- ============================================================

CREATE TABLE organization_member (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    organization_id int NOT NULL,
    user_id int NOT NULL,

    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',

    joined_at TIMESTAMPTZ,

    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT uk_organization_member_user
        UNIQUE (user_id),

    CONSTRAINT fk_organization_member_organization
        FOREIGN KEY (organization_id)
        REFERENCES organization(id)
        ON DELETE CASCADE,

    CONSTRAINT fk_organization_member_user
        FOREIGN KEY (user_id)
        REFERENCES users(id)
        ON DELETE CASCADE,


    CONSTRAINT ck_organization_member_status
        CHECK (
            status IN (
                'INVITED',
                'ACTIVE',
                'SUSPENDED',
                'REMOVED'
            )
        ),
    CONSTRAINT uk_organization_member_id_organization
    UNIQUE (id, organization_id)
);


CREATE INDEX idx_organization_member_organization_status
    ON organization_member(organization_id, status);


CREATE TABLE department_member (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

    organization_id int NOT NULL,
    department_id int NOT NULL,
    organization_member_id bigint NOT NULL,

    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',

    joined_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT uk_department_member
        UNIQUE (
            department_id,
            organization_member_id
        ),

    CONSTRAINT fk_department_member_organization
        FOREIGN KEY (organization_id)
        REFERENCES organization(id)
        ON DELETE CASCADE,

    CONSTRAINT fk_department_member_department
        FOREIGN KEY (
            department_id,
            organization_id
        )
        REFERENCES department(
            id,
            organization_id
        )
        ON DELETE CASCADE,

    CONSTRAINT fk_department_member_member
        FOREIGN KEY (
            organization_member_id,
            organization_id
        )
        REFERENCES organization_member(
            id,
            organization_id
        )
        ON DELETE CASCADE,

    CONSTRAINT ck_department_member_status
        CHECK (
            status IN (
                'ACTIVE',
                'INACTIVE',
                'REMOVED'
            )
        )

);

CREATE INDEX idx_department_member_member
    ON department_member(organization_member_id);

CREATE INDEX idx_department_member_department_status
    ON department_member(department_id, status);

CREATE INDEX idx_department_member_organization
    ON department_member(organization_id);


