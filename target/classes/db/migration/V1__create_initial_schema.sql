-- ============================================================
-- 17. ASSIGN PERMISSIONS TO ROLES
-- ============================================================

INSERT INTO role_permission (
    role_id,
    permission_id
)
SELECT
    r.id,
    p.id
FROM role r
CROSS JOIN permission p
WHERE r.name = 'ADMIN'
ON CONFLICT (role_id, permission_id) DO NOTHING;


-- VIEWER
INSERT INTO role_permission (
    role_id,
    permission_id
)
SELECT
    r.id,
    p.id
FROM role r
JOIN permission p
    ON p.code IN (
        'POST_READ',
        'MEDIA_READ',
        'SOCIAL_ACCOUNT_READ',
        'SOCIAL_CHANNEL_READ',
        'ORGANIZATION_READ',
        'ORGANIZATION_MEMBER_READ',
        'DEPARTMENT_READ'
    )
WHERE r.name = 'VIEWER'
ON CONFLICT (role_id, permission_id) DO NOTHING;


