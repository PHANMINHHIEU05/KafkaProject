-- 5. RBAC
-- ============================================================

CREATE TABLE role (
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

    name VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uk_role_name
        UNIQUE (name)
);


CREATE TABLE permission (
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

    code VARCHAR(150) NOT NULL,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(500),

    permission_group VARCHAR(100) NOT NULL,

    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT uk_permission_code
        UNIQUE (code),

    CONSTRAINT ck_permission_group
        CHECK (
            permission_group IN (
                'POST',
                'MEDIA',
                'SOCIAL_ACCOUNT',
                'SOCIAL_CHANNEL',
                'ORGANIZATION',
                'DEPARTMENT',
                'USER',
                'ROLE',
                'SYSTEM'
            )
        )
);


CREATE TABLE organization_member_role (
    organization_member_id bigint NOT NULL,
    role_id int NOT NULL,

    CONSTRAINT pk_organization_member_role
        PRIMARY KEY (
            organization_member_id,
            role_id
        ),

    CONSTRAINT fk_organization_member_role_member
        FOREIGN KEY (organization_member_id)
        REFERENCES organization_member(id)
        ON DELETE CASCADE,

    CONSTRAINT fk_organization_member_role_role
        FOREIGN KEY (role_id)
        REFERENCES role(id)
        ON DELETE CASCADE
);


CREATE TABLE role_permission (
    role_id int NOT NULL,
    permission_id int NOT NULL,

    CONSTRAINT pk_role_permission
        PRIMARY KEY (
            role_id,
            permission_id
        ),

    CONSTRAINT fk_role_permission_role
        FOREIGN KEY (role_id)
        REFERENCES role(id)
        ON DELETE CASCADE,

    CONSTRAINT fk_role_permission_permission
        FOREIGN KEY (permission_id)
        REFERENCES permission(id)
        ON DELETE CASCADE
);


CREATE INDEX idx_permission_group
    ON permission(permission_group);

CREATE INDEX idx_organization_member_role_role
    ON organization_member_role(role_id);

CREATE INDEX idx_role_permission_permission
    ON role_permission(permission_id);


