-- 13. TRANSACTIONAL OUTBOX
-- ============================================================

CREATE TABLE outbox_event (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

    organization_id int NOT NULL,

    aggregate_id bigint NOT NULL,
    aggregate_type VARCHAR(100) NOT NULL,

    event_type VARCHAR(100) NOT NULL,

    topic VARCHAR(255) NOT NULL,
    event_key VARCHAR(255) NOT NULL,

    payload JSONB NOT NULL,

    status VARCHAR(20) NOT NULL DEFAULT 'NEW',

    retry_count INTEGER NOT NULL DEFAULT 0,
    max_retry INTEGER NOT NULL DEFAULT 10,

    available_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    published_at TIMESTAMPTZ,

    error_code VARCHAR(100),
    error_message TEXT,

    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_outbox_event_organization
        FOREIGN KEY (organization_id)
        REFERENCES organization(id)
        ON DELETE RESTRICT,

    CONSTRAINT ck_outbox_status
        CHECK (
            status IN (
                'NEW',
                'PROCESSING',
                'RETRY_WAIT',
                'PUBLISHED',
                'DEAD'
            )
        ),

    CONSTRAINT ck_outbox_retry_count
        CHECK (retry_count >= 0),

    CONSTRAINT ck_outbox_max_retry
        CHECK (max_retry >= 0),

    CONSTRAINT ck_outbox_retry_limit
        CHECK (retry_count <= max_retry)
);


CREATE INDEX idx_outbox_organization_status_available
    ON outbox_event(
        organization_id,
        status,
        available_at
    );

CREATE INDEX idx_outbox_status_available
    ON outbox_event(status, available_at);

CREATE INDEX idx_outbox_aggregate
    ON outbox_event(aggregate_type, aggregate_id);

CREATE INDEX idx_outbox_topic_status
    ON outbox_event(topic, status);


