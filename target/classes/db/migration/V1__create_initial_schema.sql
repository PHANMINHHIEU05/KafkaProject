-- 8. POST
-- ============================================================

CREATE TABLE post (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

    organization_id int NOT NULL,
    department_id int,
    user_id int NOT NULL,
    title VARCHAR(500),
    content TEXT NOT NULL,
    status VARCHAR(30) NOT NULL DEFAULT 'DRAFT',
    client_request_id VARCHAR(255),
    scheduled_at TIMESTAMPTZ,
    submitted_at TIMESTAMPTZ,
    reviewed_by int,
    reviewed_at TIMESTAMPTZ,
    review_note TEXT,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_post_organization
        FOREIGN KEY (organization_id)
        REFERENCES organization(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_post_department
        FOREIGN KEY (department_id, organization_id)
        REFERENCES department(id, organization_id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_post_user
        FOREIGN KEY (user_id)
        REFERENCES users(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_post_reviewer
        FOREIGN KEY (reviewed_by)
        REFERENCES users(id)
        ON DELETE SET NULL,

    CONSTRAINT ck_post_status
        CHECK (
            status IN (
                'DRAFT',
                'PENDING_APPROVAL',
                'REJECTED',
                'APPROVED',
                'SCHEDULED',
                'QUEUED',
                'PROCESSING',
                'PARTIAL_SUCCESS',
                'PUBLISHED',
                'FAILED',
                'CANCELLED'
            )
        ),

    CONSTRAINT uk_post_client_request
        UNIQUE (
            organization_id,
            user_id,
            client_request_id
        )
);


CREATE INDEX idx_post_organization_status
    ON post(organization_id, status);

CREATE INDEX idx_post_department_status
    ON post(department_id, status);

CREATE INDEX idx_post_user_status
    ON post(user_id, status);

CREATE INDEX idx_post_status_scheduled
    ON post(status, scheduled_at);


